package com.fordev.taski.otros;

public class PorcentajesInformacion {
    int infoPersonal, infoNegocio;

    public PorcentajesInformacion() {
    }

    public PorcentajesInformacion(int infoPersonal, int infoNegocio) {
        this.infoPersonal = infoPersonal;
        this.infoNegocio = infoNegocio;
    }

    public int getInfoPersonal() {
        return infoPersonal;
    }

    public void setInfoPersonal(int infoPersonal) {
        this.infoPersonal = infoPersonal;
    }

    public int getInfoNegocio() {
        return infoNegocio;
    }

    public void setInfoNegocio(int infoNegocio) {
        this.infoNegocio = infoNegocio;
    }
}
